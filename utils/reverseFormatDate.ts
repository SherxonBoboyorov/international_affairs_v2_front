/**
 * Sana formatini teskari ko'rinishga keltirish (DD.MM.YYYY formatida)
 * @param reverseOriginalDate - Kiruvchi sana matni
 */
export default (reverseOriginalDate: null | string | undefined): string => {
  if (reverseOriginalDate) {
    const separators = ["-", ".", "/"];
    let formattedDate = reverseOriginalDate;
    for (const separator of separators) {
      if (reverseOriginalDate.includes(separator)) {
        const parts = reverseOriginalDate.split(separator);
        if (parts.length >= 3) {
          formattedDate = `${parts[2]}.${parts[1]}.${parts[0]}`;
        }
        break;
      }
    }
    return formattedDate;
  }
  return "";
};
